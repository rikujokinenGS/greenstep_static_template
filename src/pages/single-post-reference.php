                <blockquote>"Greenstepillä on asiantuntevia ihmisiä, jotka ovat motivoituneita ja sitoutuneita hoitamaan juuri meidän asiakkuutta. Yhteistyö on ollut hauskaa, ja henkilökemiat ovat kohdanneet alusta asti. Meillä on samanhenkinen tekemisen meininki, ja molemmissa organisaatioissa näkyy se, että ihmiset viihtyvät työssään"</blockquote>
                
