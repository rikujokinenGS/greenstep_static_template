                <p>Rettalla katse on suunnattu eteenpäin — lähivuosien tavoitteena on kasvaa ja laajentua erityisesti Pohjoismaissa. Luotettava taloushallinnon kumppani on tärkeä tuki kasvun matkalla.</p>
                <p>"Odotan mielenkiinnolla, millaisia mahdollisuuksia tulevat vuodet tuovat tullessaan. Tiedän, että Greenstepin suuntaan on helppo olla yhteydessä, kun uusia tarpeita nousee esiin. Voin ehdottomasti suositella Greenstepiä kumppaniksi muillekin — heillä on asiantunteva tiimi ja vahvat taustajoukot, jotka tuovat turvaa tekemiseen. Greenstepin kanssa voimme luottaa siihen, että asiat hoituvat", Krapf summaa.</p>

