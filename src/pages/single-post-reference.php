                <p>"Olemme hyödyntäneet Greenstepin osaamista aiemmin muun muassa <a href="#">IT-raportoinnissa</a> sekä <a href="#">interim-talouspäällikön ja controllerin tehtävissä</a>. Nyt päätimme syventää yhteistyötä <a href="#">taloushallinnon ulkoistamisella</a>. Alun perin konsernin emoyhtiö vastasi keskitetyistä tukitoiminnoista, talous mukaan lukien. Liiketoimintojen myynti loi kuitenkin tarpeen eriyttää näitä toimintoja ja ulkoistaa taloushallinto", alustaa Rettan talousjohtaja Sebastian Krapf.</p>

                <h2>Osaava kumppani toi sujuvuutta taloushallintoon</h2>
                <p>Kun taloushallinnon eriyttäminen tuli ajankohtaiseksi, Krapf totesi, että se tapahtuisi joustavammin ulkoistuksen kautta. Yrityskauppojen myötä aiemmat taloushallinnon järjestelmät siirtyivät uusille omistajille, joten Rettalla oli tarve ottaa käyttöön kokonaan uudet ratkaisut kirjanpitoa sekä osto- ja matkalaskuja varten.</p>
                <p>"Toiminta on kehittynyt erittäin myönteiseen suuntaan sen jälkeen, kun aloitimme yhteistyön Greenstepin kanssa. Ulkoistuksen myötä olemme saaneet asteen parempaa palvelua, ja taloushallinnon prosessit ovat nyt erinomaisesti hallinnassa. Kirjanpidon ja ostolaskujen osalta siirryimme Netvisoriin, mikä on ollut selkeä parannus aiempiin järjestelmiin verrattuna", Krapf toteaa. Koko organisaation kannalta merkittävä muutos on ollut uuden kululaskuohjelman, Bezalan, käyttöönotto.</p>
                <p>"Bezalan helppokäyttöisyys on saanut paljon kiitosta — ei pelkästään taloushallinnon näkökulmasta, vaan laajemminkin organisaation sisällä", Krapf lisää.</p>

